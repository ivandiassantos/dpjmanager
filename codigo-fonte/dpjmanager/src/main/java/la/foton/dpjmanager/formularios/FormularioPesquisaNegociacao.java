
package la.foton.dpjmanager.formularios;

public class FormularioPesquisaNegociacao
{

   private Long codigoIntegrante;
   private String dataInicio;
   private String dataFinal;

   public Long getCodigoIntegrante()
   {
      return codigoIntegrante;
   }

   public void setCodigoIntegrante(Long codigoIntegrante)
   {
      this.codigoIntegrante = codigoIntegrante;
   }

   public String getDataInicio()
   {
      return dataInicio;
   }

   public void setDataInicio(String dataInicio)
   {
      this.dataInicio = dataInicio;
   }

   public String getDataFinal()
   {
      return dataFinal;
   }

   public void setDataFinal(String dataFinal)
   {
      this.dataFinal = dataFinal;
   }

}
